import pandas as pd
import unicodedata
import re


# Normalize and clean text
def normalize_text(text):

    if pd.isna(text):

        return ""

    # Unicode normalization
    text = unicodedata.normalize(
        "NFKC",
        str(text)
    )

    # Remove page markers
    text = re.sub(
        r"\[page.*?\]",
        "",
        text,
        flags=re.IGNORECASE
    )

    # Remove ||1|| style markers
    text = re.sub(
        r"\|\|.*?\|\|",
        "",
        text
    )

    # Remove XML/HTML leftovers
    text = re.sub(
        r"<.*?>",
        "",
        text
    )

    # Remove repeated slashes
    text = re.sub(
        r"/+",
        " ",
        text
    )

    # Remove multiple spaces
    text = re.sub(
        r"\s+",
        " ",
        text
    )

    return text.strip()


# Clean dataframe
def clean_dataframe(df):

    # Clean all text columns
    text_columns = [
        col for col in df.columns
        if df[col].dtype == object
    ]

    for col in text_columns:

        df[col] = df[col].astype(str)

        df[col] = df[col].apply(
            normalize_text
        )

    # Remove unwanted metadata rows
    unwanted_patterns = [
        "copyright",
        "gretil",
        "for reference purposes only",
        "generated by",
        "input by",
        "contact",
        "disclaimer",
        "utf-8",
        "converted to",
        "this file",
        "pts page",
        "bjt page",
        "vol",
        "page 00",
    ]

    for pattern in unwanted_patterns:

        for col in text_columns:

            df = df[
                ~df[col]
                .str.lower()
                .str.contains(
                    pattern,
                    na=False
                )
            ]

    # Remove tiny junk rows
    for col in text_columns:

        df = df[
            df[col]
            .str.len() > 2
        ]

    # Remove duplicates
    df = df.drop_duplicates()

    # Remove empty rows
    df = df.dropna(
        how="all"
    )

    return df